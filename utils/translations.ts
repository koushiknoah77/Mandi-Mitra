export const getLabel = (key: string, lang: SupportedLanguageCode): string => {
  const entry = TRANSLATIONS[key];
  if (!entry) return key;
  
  // 1. Exact Match
  if (entry[lang]) return entry[lang];

  // 2. Fallback for similar languages (Scripts/Families) if specific missing
  if (lang === 'mai' || lang === 'doi' || lang === 'ks') return entry['hi'] || entry['en']; // Hindi-adjacent
  if (lang === 'as') return entry['bn'] || entry['en']; // Bengali-adjacent script
  if (lang === 'kok') return entry['mr'] || entry['en']; // Marathi-adjacent
  
  // 3. Fallback to English
  return entry['en'] || key;
};
